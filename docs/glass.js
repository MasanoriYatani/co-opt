/**
 * Calculate refractive index using Sellmeier equation
 * @param {Object} sellmeierCoeffs - Sellmeier coefficients {A1, A2, A3, B1, B2, B3}
 * @param {number} wavelength - Wavelength in micrometers (m)
 * @returns {number} Calculated refractive index
 */
export function calculateRefractiveIndex(sellmeierCoeffs, wavelength) {
  if (!sellmeierCoeffs || wavelength <= 0) {
    return 1.0;
  }
  
  const { A1, A2, A3, B1, B2, B3 } = sellmeierCoeffs;
  const lambdaSquared = wavelength * wavelength;
  
  try {
    const n2 = 1 + 
      (A1 * lambdaSquared) / (lambdaSquared - B1) +
      (A2 * lambdaSquared) / (lambdaSquared - B2) +
      (A3 * lambdaSquared) / (lambdaSquared - B3);
    
    const n = Math.sqrt(n2);
    
    // Sanity check
    if (!isFinite(n) || n < 1.0 || n > 3.0) {
      console.warn(` Calculated refractive index ${n} seems invalid for wavelength ${wavelength}m`);
      return 1.0;
    }
    
    return n;
  } catch (error) {
    console.warn(` Error calculating refractive index: ${error.message}`);
    return 1.0;
  }
}

/**
 * Get glass data with Sellmeier coefficients from all databases
 * @param {string} glassName - Name of the glass material (or numeric string for direct refractive index)
 * @returns {Object|null} Glass data with Sellmeier coefficients or null if not found
 */
export function getGlassDataWithSellmeier(glassName) {
  if (!glassName) return null;
  
  // Check if glassName is a numeric value (e.g., "1.5", "1.517")
  const numericValue = parseFloat(glassName);
  if (!isNaN(numericValue) && numericValue > 0 && numericValue < 4) {
    // Create synthetic glass data with constant refractive index
    // n = 1 + A1, so A1 = n - 1
    const A1 = numericValue * numericValue - 1;
    return {
      name: glassName,
      nd: numericValue,
      vd: undefined, // No dispersion (Abbe number not applicable)
      sellmeier: {
        A1: A1,
        A2: 0,
        A3: 0,
        B1: 0,
        B2: 0,
        B3: 0
      }
    };
  }
  
  // Search in miscellaneousDB first
  let glassData = miscellaneousDB.find(glass => glass.name === glassName);
  if (glassData) return glassData;
  
  // Search in oharaGlassDB
  glassData = oharaGlassDB.find(glass => glass.name === glassName);
  if (glassData) return glassData;
  
  // Search in schottGlassDB
  glassData = schottGlassDB.find(glass => glass.name === glassName);
  if (glassData) return glassData;
  
  return null;
}

/**
 * Get all glass databases for searching
 * @returns {Array} Array of all glass databases
 */
export function getAllGlassDatabases() {
  return [miscellaneousDB, oharaGlassDB, schottGlassDB];
}

/**
 * Find similar glasses by nd/vd (Abbe) using a simple weighted distance.
 * Returns an array sorted by closest first.
 *
 * @param {number} targetNd
 * @param {number} targetVd
 * @param {number} [maxResults=20]
 * @returns {Array<{name:string, nd:number, vd:number, ndDiff:number, vdDiff:number, totalDiff:number}>}
 */
export function findSimilarGlassesByNdVd(targetNd, targetVd, maxResults = 20) {
  if (!Number.isFinite(targetNd) || !Number.isFinite(targetVd)) return [];
  if (targetNd <= 0 || targetNd >= 4) return [];
  if (targetVd <= 0) return [];

  const dbs = getAllGlassDatabases();
  /** @type {Array<{name:string, nd:number, vd:number, ndDiff:number, vdDiff:number, totalDiff:number}>} */
  const out = [];

  for (const db of dbs) {
    if (!Array.isArray(db)) continue;
    for (const glass of db) {
      if (!glass) continue;
      const nd = glass.nd;
      const vd = glass.vd;
      if (!Number.isFinite(nd) || !Number.isFinite(vd)) continue;
      // Match legacy UI behavior: nd is far more sensitive than vd.
      const ndDiff = nd - targetNd;
      const vdDiff = vd - targetVd;
      const totalDiff = Math.abs(ndDiff) * 10 + Math.abs(vdDiff);
      out.push({
        name: String(glass.name),
        nd,
        vd,
        ndDiff,
        vdDiff,
        totalDiff
      });
    }
  }

  out.sort((a, b) => a.totalDiff - b.totalDiff);
  const n = Number(maxResults);
  const limit = Number.isFinite(n) && n > 0 ? Math.floor(n) : 20;
  return out.slice(0, limit);
}

function __normalizeGlassNameForSearch(s) {
  return String(s ?? '')
    .toLowerCase()
    .replace(/\s+/g, '')
    .replace(/[^a-z0-9]/g, '');
}

function __levenshteinDistance(a, b, maxDistance = 32) {
  const s = String(a ?? '');
  const t = String(b ?? '');
  if (s === t) return 0;
  const n = s.length;
  const m = t.length;
  if (n === 0) return m;
  if (m === 0) return n;

  // Quick bound check
  if (Math.abs(n - m) > maxDistance) return maxDistance + 1;

  /** @type {number[]} */
  let prev = new Array(m + 1);
  /** @type {number[]} */
  let cur = new Array(m + 1);
  for (let j = 0; j <= m; j++) prev[j] = j;

  for (let i = 1; i <= n; i++) {
    cur[0] = i;
    let rowMin = cur[0];
    const sc = s.charCodeAt(i - 1);
    for (let j = 1; j <= m; j++) {
      const cost = sc === t.charCodeAt(j - 1) ? 0 : 1;
      const del = prev[j] + 1;
      const ins = cur[j - 1] + 1;
      const sub = prev[j - 1] + cost;
      const v = Math.min(del, ins, sub);
      cur[j] = v;
      if (v < rowMin) rowMin = v;
    }
    if (rowMin > maxDistance) return maxDistance + 1;
    const tmp = prev;
    prev = cur;
    cur = tmp;
  }
  return prev[m];
}

/**
 * Suggest glass names by fuzzy-ish name similarity (for unknown material names).
 *
 * @param {string} query
 * @param {number} [maxResults=20]
 * @returns {Array<{name:string, score:number}>}
 */
export function findSimilarGlassNames(query, maxResults = 20) {
  const q = String(query ?? '').trim();
  if (q === '') return [];

  const qn = __normalizeGlassNameForSearch(q);
  if (qn === '') return [];

  const dbs = getAllGlassDatabases();
  /** @type {Array<{name:string, score:number}>} */
  const out = [];

  for (const db of dbs) {
    if (!Array.isArray(db)) continue;
    for (const g of db) {
      if (!g || typeof g.name !== 'string') continue;
      const name = g.name;
      const nn = __normalizeGlassNameForSearch(name);
      if (nn === '') continue;

      // Strong signal: substring match (normalized)
      let score;
      if (nn.includes(qn) || qn.includes(nn)) score = 0;
      else score = __levenshteinDistance(qn, nn, 24);

      out.push({ name, score });
    }
  }

  out.sort((a, b) => a.score - b.score || a.name.localeCompare(b.name));
  const n = Number(maxResults);
  const limit = Number.isFinite(n) && n > 0 ? Math.floor(n) : 20;
  return out.slice(0, limit);
}

/**
 * Get primary wavelength from Source table
 * @returns {number} Primary wavelength in micrometers (m)
 */
export function getPrimaryWavelength() {
  const DEBUG = !!(typeof globalThis !== 'undefined' && (globalThis.__PSF_DEBUG || globalThis.__OPD_DEBUG));
  if (DEBUG) console.log(' getPrimaryWavelength called from glass.js');
  try {
    // tableSource
    if (typeof window !== 'undefined' && window.tableSource && typeof window.tableSource.getData === 'function') {
      const sourceData = window.tableSource.getData();
      if (DEBUG) console.log(' Source data (glass.js):', sourceData);
      
      // Primary Wavelength
      const primaryEntry = sourceData.find(row => row.primary === "Primary Wavelength");
      if (DEBUG) console.log(' Primary entry found (glass.js):', primaryEntry);
      
      if (primaryEntry && primaryEntry.wavelength) {
        const wavelength = parseFloat(primaryEntry.wavelength);
        if (!isNaN(wavelength)) {
          if (DEBUG) console.log(` Primary wavelength found (glass.js): ${wavelength} m`);
          return wavelength;
        }
      }
      
      // d
      if (DEBUG) console.log(' Primary wavelength not found (glass.js), using default: 0.5876 m');
      return 0.5876;
    } else {
      if (DEBUG) console.warn(' window.tableSource not available in glass.js');
    }
  } catch (error) {
    if (DEBUG) console.warn(' Error getting primary wavelength (glass.js):', error);
  }
  
  // 
  return 0.5876;
}

